//Write a program to input two numbers and display their sum
#include<stdio.h>
int main()
{
    int a,b,c;
    printf("Enter the first number:\n");
    scanf("%d",&a);
    printf("Enter the second number:\n");
    scanf("%d",&b);
    c=a+b;
    printf("The sum of the two numbers you entered is %d\n",c);
    return 0;

}
